pub fn getUnitDef(unit_type: UnitType) *const UnitDef {
    for (&UNIT_DATABASE) |*def| {
        if (def.unit_type == unit_type) return def;
    }
    return &UNIT_DATABASE[0];
}

pub fn getUnitCost(unit_type: UnitType) u32 {
    return getUnitDef(unit_type).cost;
}

pub fn getUnitBuildTime(unit_type: UnitType) f32 {
    return getUnitDef(unit_type).build_time;
}
