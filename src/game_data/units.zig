    health_bonus: [9]f32 = .{ 0.0, 0.1, 0.15, 0.2, 0.25, 0.3, 0.35, 0.4, 0.5 },
    damage_bonus: [9]f32 = .{ 0.0, 0.1, 0.15, 0.2, 0.25, 0.3, 0.35, 0.4, 0.5 },
    armor_bonus: [9]u32 = .{ 0, 1, 2, 3, 4, 5, 6, 7, 10 },
